<p><strong>问：分布式系统如何保证数据一致性？</strong></p>
<p>答：在分布式系统中，保证数据一致性通常有以下几种方案：</p>
<ol>
<li><strong>强一致性</strong>：在强一致性的模型中，系统保证所有节点上的数据始终保持一致。传统的分布式数据库（如 Google Spanner）采用两阶段提交（2PC）协议来实现强一致性。这种方式确保所有操作在系统中的所有副本上同步更新，通常会导致较高的延迟，适用于对一致性要求较高的场景。</li>
<li><strong>最终一致性</strong>：最终一致性是一种弱一致性模型，允许在某个时间点不同节点上的数据不一致，但最终会通过某种机制（如数据同步、异步消息队列等）使得所有节点上的数据最终一致。很多 NoSQL 数据库（如 Cassandra、MongoDB）采用这种一致性模型，适用于对高可用性和高性能要求较高的系统。</li>
<li><strong>分布式事务</strong>：在分布式系统中，跨多个节点的事务通常使用某些协议来保证一致性，如：
<ul>
<li><strong>两阶段提交协议（2PC）</strong>：用于协调多个参与节点的事务提交，确保所有节点的事务一致性。尽管 2PC 可以保证一致性，但它可能在出现网络故障时导致阻塞和性能瓶颈。</li>
<li><strong>三阶段提交协议（3PC）</strong>：在两阶段提交协议的基础上，通过增加一个准备阶段来避免部分节点因故障而处于阻塞状态，从而提高容错性。</li>
</ul>
</li>
<li><strong>事务日志与补偿机制</strong>：在分布式系统中，通常使用事务日志来记录每一个操作步骤。在出现故障时，通过回滚或补偿操作来恢复数据的一致性。Saga 模式就是一种分布式事务的补偿机制，它将一个大事务分解为多个小事务，通过补偿机制来保证事务的一致性。</li>
</ol>
